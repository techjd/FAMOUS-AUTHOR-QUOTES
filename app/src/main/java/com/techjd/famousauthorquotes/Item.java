package com.techjd.famousauthorquotes;



public class Item {

    private String mImageUrl;
    private String mCreator;

    public Item(String imageUrl, String creator) {
        mImageUrl = imageUrl;
        mCreator = creator;
    }

    public String getmImageUrl() {
        return mImageUrl;
    }



    public String getmCreator() {
        return mCreator;
    }


}
